let v1 = Number(prompt("Escreva o valor 1: "))
let v2 = Number(prompt("Escreva o valor 2: "))
let soma = v1+v2
let sub = v1-v2
let mul = v1*v2
let div = v1/v2

document.write(`A soma dos dois valores é ${soma.toFixed(2)}`)
document.write("</br>")
document.write(`A subtração dos dois valores é ${sub.toFixed(2)}`) 
document.write("</br>")
document.write(`A multiplicação dos dois valores é ${mul.toFixed(2)}`)
document.write("</br>")
document.write(`A divisão dos dois valores(valor 1 / valor 2) é ${div.toFixed(2)}`)




